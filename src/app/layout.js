"use client";
import "./global.css";
import { useState } from "react";
import { Fira_Code, Nunito_Sans } from "next/font/google";
import { ThemeProvider } from "./components/theme-provider";

import Navbar from "./components/Navbar";
import { AuthContextProvider } from "./context/AuthContext";

const firacode = Fira_Code({
  subsets: ["latin"],
  display: "swap",
  variable: "--font-firacode",
});

const nunito = Nunito_Sans({
  subsets: ["latin"],
  display: "swap",
  variable: "--font-nunito",
});

export const metadata = {
  title: "Intrinsically Motivating Courses",
  description: "Generated by create next app",
  icons: {
    icon: "/icon.ico",
  },
};

export default function RootLayout({ children }) {
  const [isChecked, setIsChecked] = useState(false);
  const handleCheck = (event) => {
    setIsChecked(event.target.checked);
    console.log("Checkbox is checked:", event.target.checked);
  };

  function pageBackground() {
    isChecked ? "bg-background/80 backdrop-blur-sm" : null;
  }
  return (
    <html
      lang="en"
      className={`${nunito.variable} ${firacode.variable} font-nunito`}
    >
      <body>
        <AuthContextProvider>
          <ThemeProvider
            attribute="class"
            defaultTheme="system"
            enableSystem
            disableTransitionOnChange
          >
            <Navbar handleCheck={handleCheck} />
            {children}
          </ThemeProvider>
        </AuthContextProvider>
      </body>
    </html>
  );
}
